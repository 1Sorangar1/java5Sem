package module8;

import java.util.Arrays;

public class Practice1 {
    public static void main(String[] args) {
        int a1[] = {1, 2, 3, 4, 5};
        int a2[] = {6, 7, 8, 9, 10};

        // Присваиваем ссылку a2 массиву a1
        a1 = a2;

        /*
        Теперь переменная a1 ссылается на тот же массив, что и a2.
        Старый массив, на который ссылалась a1 ({1, 2, 3, 4, 5}), становится недостижимым.
        */
        System.out.println("Массив a1: " + Arrays.toString(a1)); // [6, 7, 8, 9, 10]
        System.out.println("Массив a2: " + Arrays.toString(a2)); // [6, 7, 8, 9, 10]
    }
}
/*

После a1 = a2 обе переменные a1 и a2 ссылаются на один и тот же массив {6, 7, 8, 9, 10}.
Старый массив {1, 2, 3, 4, 5} становится недоступным и будет удален сборщиком мусора.
Это пример работы со ссылками на объекты (массивы).
*/
