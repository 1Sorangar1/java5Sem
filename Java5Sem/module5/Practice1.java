package module5;

public class Practice1 {
    public static void main(String[] args) {
        int a = 2; // Пример значения
        /*
         Формат: условие ? значение_если_условие_верно : значение_если_условие_неверно.
         */
        int i = (a == 1) ? 1 : (a == 2) ? 2 : 3;

        System.out.println("Значение i: " + i);
    }
}
