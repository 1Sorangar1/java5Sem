package module11;

/**
 * Класс демонстрирует использование статических методов и полей.
 */
public class A {
    public static int a = 1; // Статическое поле a
    public static int b;    // Статическое поле b

    /**
     * Статический метод для вывода значений статических переменных.
     */
    public static void printVars() {
        System.out.println("Static variable a: " + a);
        System.out.println("Static variable b: " + b);
    }
}

/**
 * Основной класс для демонстрации всех способов вызова метода printVars().
 */
class Practice1 {
    public static void main(String[] args) {
        // Вызов через имя класса
        A.printVars();

        // Вызов через объект класса
        A obj = new A();
        obj.printVars();

        // Динамическое изменение статических полей
        A.a = 10;
        A.b = 20;

        // Повторный вызов через имя класса после изменения
        A.printVars();

        // Вызов через новый объект
        A anotherObj = new A();
        anotherObj.printVars();
    }
}
/*

Статический метод и поле:

Поля a и b являются статическими, их значения общие для всех объектов.
Метод printVars() выводит значения этих полей и также является статическим.
Способы вызова метода printVars():

Через имя класса: A.printVars();
Это наиболее правильный способ вызова статических методов и отображает их принадлежность классу.
Через объект класса:
obj.printVars();
Несмотря на то, что метод вызывается через объект, он всё равно остаётся статическим и связан с классом.
Динамическое изменение статических полей:

Изменения значений полей a и b через класс A или любой объект затрагивают все другие объекты.
Это демонстрирует, что статические поля являются общими для всех экземпляров класса.
*/
